import { toast } from "react-toastify";

export const handleNotificationError = (mensaje) => {
  toast.error(mensaje, {
    position: "top-right",
    autoClose: 1500,
    hideProgressBar: false,
    closeOnClick: true,
    pauseOnHover: true,
    draggable: true,
    progress: undefined,
    theme: "colored",
  });
};


